    private void tabelAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAdminMouseClicked
        // TODO add your handling code here:
        int baris = tabelAdmin.rowAtPoint(evt.getPoint());
        String id = tabelAdmin.getValueAt(baris, 1).toString();
        txtid.setText(id);
        
        String nama = tabelAdmin.getValueAt(baris, 2).toString();
        txtNama.setText(nama);
        
        String username = tabelAdmin.getValueAt(baris, 3).toString();
        txtUsername.setText(username);
        
        String password = tabelAdmin.getValueAt(baris, 4).toString();
        txtPassword.setText(password);
        
    }//GEN-LAST:event_tabelAdminMouseClicked

    private void tbEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbEditActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "UPDATE user SET id='"+txtid.getText()+"',nama='"+txtNama.getText()+"',username='"+txtUsername.getText()+"',password='"+txtPassword.getText()+"' WHERE id = '"+txtid.getText()+"'";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil...");
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        tampilkan_data();
        kosongkan_form();
    }//GEN-LAST:event_tbEditActionPerformed

